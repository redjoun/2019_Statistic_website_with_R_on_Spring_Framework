package com.ch.stat.model;

public class MobileDB {
	private int		mobileDB_id;
	private String 	mobileDB_value;
	private int		mobileDB_people;
	private int		mobileDB_percent;
	
	public int getMobileDB_id() {
		return mobileDB_id;
	}
	public void setMobileDB_id(int mobileDB_id) {
		this.mobileDB_id = mobileDB_id;
	}
	public String getMobileDB_value() {
		return mobileDB_value;
	}
	public void setMobileDB_value(String mobileDB_value) {
		this.mobileDB_value = mobileDB_value;
	}
	public int getMobileDB_people() {
		return mobileDB_people;
	}
	public void setMobileDB_people(int mobileDB_people) {
		this.mobileDB_people = mobileDB_people;
	}
	public int getMobileDB_percent() {
		return mobileDB_percent;
	}
	public void setMobileDB_percent(int mobileDB_percent) {
		this.mobileDB_percent = mobileDB_percent;
	}
	
}
