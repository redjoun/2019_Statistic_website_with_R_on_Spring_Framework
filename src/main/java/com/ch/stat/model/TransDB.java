package com.ch.stat.model;

public class TransDB {

	private int		transDB_id;
	private String	transDB_value;
	private int		transDB_percent;
	
	public int getTransDB_id() {
		return transDB_id;
	}
	public void setTransDB_id(int transDB_id) {
		this.transDB_id = transDB_id;
	}
	public String getTransDB_value() {
		return transDB_value;
	}
	public void setTransDB_value(String transDB_value) {
		this.transDB_value = transDB_value;
	}
	public int getTransDB_percent() {
		return transDB_percent;
	}
	public void setTransDB_percent(int transDB_percent) {
		this.transDB_percent = transDB_percent;
	}
	
}
