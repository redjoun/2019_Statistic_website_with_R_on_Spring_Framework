package com.ch.stat.model;

public class DigitalDB {
	private int		digitalDB_id;
	private String	digitalDB_value;
	private int		digitalDB_percent;
	
	public int getDigitalDB_id() {
		return digitalDB_id;
	}
	public void setDigitalDB_id(int digitalDB_id) {
		this.digitalDB_id = digitalDB_id;
	}
	public String getDigitalDB_value() {
		return digitalDB_value;
	}
	public void setDigitalDB_value(String digitalDB_value) {
		this.digitalDB_value = digitalDB_value;
	}
	public int getDigitalDB_percent() {
		return digitalDB_percent;
	}
	public void setDigitalDB_percent(int digitalDB_percent) {
		this.digitalDB_percent = digitalDB_percent;
	}
	
}
